import streamlit as st
import pandas as pd
import requests
import matplotlib.pyplot as plt

# Endpoint de la API
url = "https://restcountries.com/v3.1/all"

# Solicitud GET
response = requests.get(url)
if response.status_code == 200:
    data = response.json()

    # Extraer informacion relevante
    countries = []
    for country in data:
        countries.append({
            "Name": country.get("name",{}).get("common","N/A"),
            "Region": country.get("region","N/A"),
            "Population": country.get("population",0),
            "Area (Km)": country.get("area",0),
            "Borders Count": len(country.get("borders", [])),
            "Languages Count": len(country.get("language", {})),
            "Timezones Count": len(country.get("timezones", []))
        })
        
    # Convertir a DataFrame
    df = pd.DataFrame(countries)
   
    
    # Titulo de la Descripcion
    st.title('Sección de Descripcion')
   
    # Texto de la Descripcion
    st.text("Descripcion")



    #st.selectbox("SElecciona algo:",["hola", "chao"])

    
    # Título de la aplicación
    st.title('Aplicación Web: Datos desde una API REST')
    st.write(df)

    # Menu de Seleccion
    st.selectbox("Selecciona una columna:",["Name", "Region", "Population", "Area"])
    
    # Mostrar los primeros registros
    st.write('Datos obtenidos de la API:')
    st.write(df.head())
    
    # Seleccionar una columna para mostrar en Streamlit
    columnas = st.multiselect('Selecciona las columnas a visualizar',
    df.columns.tolist(), default=df.columns.tolist())
    df_seleccionado = df[columnas]
    
    # Mostrar el DataFrame con las columnas seleccionadas
    st.write('Datos seleccionados:')
    st.write(df_seleccionado)
    
